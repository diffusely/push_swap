#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == 0)
		write(fd, "0", 1);
	else if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n *= -1;
			write(fd, "-", 1);
		}
		c = (n % 10) + '0';
		n /= 10;
		if (n == 0)
		{
			write(fd, &c, 1);
			return ;
		}
		ft_putnbr_fd(n, fd);
		write(fd, &c, 1);
	}
}
